import { describe, it, expect } from 'vitest';
import { pluralize } from './pluralize';

describe('pluralize', () => {
  const forms = ['секунда', 'секунды', 'секунд'] as const;

  it.each([
    [0, 'секунд'],
    [1, 'секунда'],
    [2, 'секунды'],
    [3, 'секунды'],
    [4, 'секунды'],
    [5, 'секунд'],
    [10, 'секунд'],
    [11, 'секунд'],
    [12, 'секунд'],
    [14, 'секунд'],
    [19, 'секунд'],
    [20, 'секунд'],
    [21, 'секунда'],
    [22, 'секунды'],
    [25, 'секунд'],
    [100, 'секунд'],
    [101, 'секунда'],
    [111, 'секунд'],
    [112, 'секунд'],
    [121, 'секунда'],
    [132, 'секунды'],
  ])('pluralize(%i) → %s', (n, expected) => {
    expect(pluralize(n, ...forms)).toBe(expected);
  });

  it('works with повторение forms', () => {
    expect(pluralize(1, 'повторение', 'повторения', 'повторений')).toBe('повторение');
    expect(pluralize(3, 'повторение', 'повторения', 'повторений')).toBe('повторения');
    expect(pluralize(5, 'повторение', 'повторения', 'повторений')).toBe('повторений');
  });
});
